#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	size_t	len;

	len = ft_strlen(s1);
	str = ft_strnew(len);
	if (!str)
		return (NULL);
	ft_strcpy(str, s1);
	return (str);
}
